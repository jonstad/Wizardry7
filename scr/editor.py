def print_character(ch):
    print("=====================================")
    print(f"Name: {ch['name']}")
    print(f"Class: {ch['class']}   Level: {ch['level']}   XP: {ch['xp']}")
    print(f"HP: {ch['hp']}   Stamina: {ch['stamina']}")
    print("Stats:")
    for k, v in ch["stats"].items():
        print(f"  {k}: {v}")

    print("\nInventory:")
    for item_id, name, charges in ch["inventory"]:
        print(f"  - {name} (0x{item_id:04X}, charges={charges})")

    print("\nSpells:")
    for school, lst in ch["spells"].items():
        if lst:
            print(f"  {school}: {', '.join(lst)}")

