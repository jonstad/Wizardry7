# ------------------------------------------------------------
#  Utility
# ------------------------------------------------------------

def read_u16(data, offset):
    return struct.unpack_from("<H", data, offset)[0]

def read_u32(data, offset):
    return struct.unpack_from("<I", data, offset)[0]

def read_s16(data, offset):
    return struct.unpack_from("<h", data, offset)[0]

def read_str(data, offset, length):
    raw = data[offset:offset+length]
    return raw.split(b"\x00")[0].decode("ascii", errors="ignore")

# ------------------------------------------------------------
#  Character block detection
# ------------------------------------------------------------

def find_character_blocks(data):
    """
    Wizardry 7 DOS stores 6 character blocks of fixed size.
    But offsets vary depending on save version.
    Vi skanner etter kjente signaturer:
    - Navn (ASCII)
    - Klassefelt
    - Stats
    - Inventory count
    """
    blocks = []
    block_size = 0x180  # stabilt i DOS-versjonen

    for off in range(0, len(data) - block_size, 4):
        name = read_str(data, off, 16)
        if len(name) == 0:
            continue

        # plausibel klasse?
        cls = data[off + 0x20]
        if cls > 20:
            continue

        # plausibel HP?
        hp = read_s16(data, off + 0x30)
        if hp < 0 or hp > 500:
            continue

        # inventory count plausibel?
        inv_count = data[off + 0x80]
        if inv_count <= 20:
            blocks.append(off)

    # returner maks 6 blokker
    return blocks[:6]

# ------------------------------------------------------------
#  Spell decoding
# ------------------------------------------------------------

def decode_spell_mask(mask, spell_list):
    spells = []
    for i, name in enumerate(spell_list):
        if mask & (1 << i):
            spells.append(name)
    return spells

# ------------------------------------------------------------
#  Inventory decoding
# ------------------------------------------------------------

def decode_inventory(data, off):
    inv = []
    count = data[off + 0x80]
    ptr = off + 0x82

    for i in range(count):
        item_id = read_u16(data, ptr)
        charges = data[ptr + 2]
        ptr += 4

        name = ITEMS.get(item_id, f"Unknown(0x{item_id:04X})")
        inv.append((item_id, name, charges))

    return inv

# ------------------------------------------------------------
#  Character decoding
# ------------------------------------------------------------

def decode_character(data, off):
    name = read_str(data, off, 16)
    race = data[off + 0x10]
    gender = data[off + 0x11]
    alignment = data[off + 0x12]
    cls = data[off + 0x20]

    # HP / stamina
    hp = read_s16(data, off + 0x30)
    stamina = read_s16(data, off + 0x32)

    # Stats
    stats = {
        "STR": data[off + 0x40],
        "INT": data[off + 0x41],
        "PIE": data[off + 0x42],
        "VIT": data[off + 0x43],
        "DEX": data[off + 0x44],
        "SPD": data[off + 0x45],
        "LUC": data[off + 0x46],
    }

    # Spells
    mage_mask = read_u32(data, off + 0x60)
    priest_mask = read_u32(data, off + 0x64)
    psionic_mask = read_u32(data, off + 0x68)
    alch_mask = read_u32(data, off + 0x6C)
    bard_mask = read_u32(data, off + 0x70)
    ranger_mask = read_u32(data, off + 0x74)
    monk_mask = read_u32(data, off + 0x78)

    spells = {
        "Mage": decode_spell_mask(mage_mask, MAGE_SPELLS),
        "Priest": decode_spell_mask(priest_mask, PRIEST_SPELLS),
        "Psionic": decode_spell_mask(psionic_mask, PSIONIC_SPELLS),
        "Alchemist": decode_spell_mask(alch_mask, ALCHEMIST_SPELLS),
        "Bard": decode_spell_mask(bard_mask, BARD_SPELLS),
        "Ranger": decode_spell_mask(ranger_mask, RANGER_SPELLS),
        "Monk": decode_spell_mask(monk_mask, MONK_SPELLS),
    }

    # Inventory
    inventory = decode_inventory(data, off)

    return {
        "offset": off,
        "name": name,
        "race": race,
        "gender": gender,
        "alignment": alignment,
        "class": cls,
        "hp": hp,
        "stamina": stamina,
        "stats": stats,
        "spells": spells,
        "inventory": inventory,
    }

# ------------------------------------------------------------
#  Party table (for XP + level)
# ------------------------------------------------------------

def decode_party_table(data):
    """
    Party table ligger i DOS-versjonen rundt offset 0x2000–0x2400.
    Vi skanner etter 6 entries med plausibel XP.
    """
    entries = []
    for off in range(0x1000, 0x4000, 4):
        xp = read_u32(data, off)
        if xp < 10000000:  # plausibel XP
            entries.append((off, xp))
            if len(entries) == 6:
                break
    return [xp for (_, xp) in entries]

def xp_to_level(xp):
    """
    Wizardry 7 levelkurve (forenklet):
    Level 1 = 0
    Level 2 = 1000
    Level 3 = 3000
    Level 4 = 6000
    Level 5 = 10000
    osv.
    """
    lvl = 1
    need = 1000
    while xp >= need:
        lvl += 1
        need += lvl * 1000
    return lvl

# ------------------------------------------------------------
#  Main loader
# ------------------------------------------------------------

def load_save(filename):
    with open(filename, "rb") as f:
        data = f.read()

    blocks = find_character_blocks(data)
    party_xp = decode_party_table(data)

    chars = []
    for i, off in enumerate(blocks):
        ch = decode_character(data, off)
        ch["xp"] = party_xp[i] if i < len(party_xp) else 0
        ch["level"] = xp_to_level(ch["xp"])
        chars.append(ch)

    return chars

